    public void searchTableLoad(SetOfBooks searchList)
    {
        String[] colName = { "Accession Number", "ISBN Number", "Title", "Author "};
            Object[][] object = new Object[searchList.size()][4];
            System.out.println(searchList.size());
            int i = 0;
            if (searchList.size() != 0) {
                for (Book book : searchList) {
                    object[i][0] = book.getAccessionNumber();
                    object[i][1] = book.getISBNNumber();
                    object[i][2] = book.getTitle();
                    object[i][3] = book.getAuthor();
                    i++;
                  
                }    
            }
            DefaultTableModel model = new DefaultTableModel(object,colName)
                    {
    public boolean isCellEditable(int row, int column)
    {
      return false;//This causes all cells to be not editable
    }
  };
            tbInfoTable.setModel(model);
            tbInfoTable.setRowSorter(new TableRowSorter<TableModel> (model));
            tbInfoTable.setAutoscrolls(true);
            tbInfoTable.getTableHeader().setReorderingAllowed(false);
        
    }
  
