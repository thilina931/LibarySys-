    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
         if (lblUpID.getText().equals("--")) {

            JOptionPane.showMessageDialog(this, "Please select a book to Delete");
            return ;
        }
        int response = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this book?", "Confirm",
            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (response == JOptionPane.YES_OPTION)
        {
            Book book=holdings.findBookFromAccNumber(lblUpID.getText()).firstElement();
            if(book.isOnLoan())
            {
                JOptionPane.showMessageDialog(rootPane, "Book cannot be deleted. Book is on Loan !");
            }
            else
            {
                holdings.removeBook(book);

                //table load in search page
                TableLoad(tbInfoTable,holdings);
                //table load in update page
                TableLoad(tbInfo1Table,holdings);

                try {
                    Serialization.Serialize(holdings,"Books.txt");
                }
                catch (Exception ex) {

                }
                JOptionPane.showMessageDialog(rootPane, "Book is been deleted successfully !");
                // clear all fields

                txtUpdateTitle.setText(null);
                txtUpdateISBN.setText(null);
                txtUpdateAuthor.setText(null);
                lblUpID.setText("--");
            }
        }

        else
        {
            JOptionPane.showMessageDialog(this, "Delete Unsuccessful");
        }
        
        
        ////////////////////////////////
    }//GEN-LAST:event_btnDeleteMouseClicked
